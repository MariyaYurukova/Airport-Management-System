package airportmanagement.system.domain.models.views;

public class AirportViewModel {


    private String id;
    private String name;

    public AirportViewModel() {
    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
